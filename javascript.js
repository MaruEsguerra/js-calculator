// Basic math functions
function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

// Testing
let a = 1;
let b = 2;
sum = add(a, b);
difference = subtract(a, b);
product = multiply(a, b);
quotient = divide(a, b);
console.log(sum, difference, product, quotient);